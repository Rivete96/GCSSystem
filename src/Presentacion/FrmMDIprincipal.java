   /**
     * Abre la vista principal de proyectos del jefe.
     */
    public void AbrirFrmProyectoJefe() {
        
        int x = (Escritorio.getWidth());
        int y = (Escritorio.getHeight()); 
        FrmProyectoJefe proyectosjefe = new FrmProyectoJefe();
        Escritorio.removeAll();
        Escritorio.updateUI();
        if (proyectosjefe.isShowing()) 
        {
            proyectosjefe.setLocation(x, y);
        } 
        else 
        {
            Escritorio.add(proyectosjefe);
            proyectosjefe.setBounds(0,0,x, y);
            proyectosjefe.FormularioPrincipalJefe(this);
            proyectosjefe.setVisible(true);            
        }
    }
    
